let currentProduct = null;
let currentShade = null;

const productImg = document.getElementById("productImg");
const shadeTitle = document.getElementById("shadeTitle");
const shadeDesc = document.getElementById("shadeDesc");
const badge = document.getElementById("badge");

const productTitle = document.getElementById("productTitle");
const productPrice = document.getElementById("productPrice");
const buyText = document.getElementById("buyText");
const buyLink = document.getElementById("buyLink");
const swatchesEl = document.getElementById("swatches");

function renderSwatches(product, activeShadeId) {
  swatchesEl.innerHTML = "";

  product.shades.forEach((shade) => {
    const btn = document.createElement("button");
    btn.type = "button";
    btn.className = "swatch";

    btn.style.setProperty("--c", shade.color);

    btn.setAttribute("aria-label", `${shade.id} ${shade.name}`);
    btn.setAttribute("aria-selected", String(shade.id === activeShadeId));

    if (shade.id === activeShadeId) {
      btn.classList.add("is-active");
    }

    if (shade.badge && shade.badge.trim()) {
      btn.classList.add("has-badge");
    }

    btn.addEventListener("click", () => {
      setShade(shade.id);
    });

    swatchesEl.appendChild(btn);
  });
}


function setProduct(pid) {
  const product = products.find((p) => p.pid === pid);
  if (!product) return;

  currentProduct = product;

  productTitle.textContent = product.title;
  productPrice.textContent = product.price;
  buyText.textContent = product.buyText;

  if (buyLink) {
    buyLink.href = product.buyLink || "#";
    buyLink.target = "_blank";
    buyLink.rel = "noopener noreferrer";
  }

  if (product.shades.length > 0) {
    setShade(product.shades[0].id);
  } else {
    swatchesEl.innerHTML = "";
  }
}

async function setShade(shadeId) {
  if (!currentProduct) return;

  const shade = currentProduct.shades.find((s) => s.id === shadeId);
  if (!shade) return;

  try {
    await preloadImage(shade.img);
  } catch (e) {
    console.error("이미지 로드 실패:", shade.img);
  }

  currentShade = shade;

  productImg.src = shade.img;
  productImg.alt = `${shade.name} 이미지`;

  shadeTitle.textContent = `[${shade.id}] ${shade.name}`;
  shadeDesc.textContent = shade.desc;

  if (shade.badge && shade.badge.trim()) {
    badge.textContent = shade.badge;
    badge.style.display = "inline-flex";
  } else {
    badge.textContent = "";
    badge.style.display = "none";
  }

  renderSwatches(currentProduct, shade.id);
}

window.addEventListener("DOMContentLoaded", () => {
  const params = new URLSearchParams(location.search);
  const pid = params.get("pid") || "base-1-1";
  setProduct(pid);
});

